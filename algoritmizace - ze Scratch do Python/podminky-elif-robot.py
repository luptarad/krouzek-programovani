odpoved_uzivatele = input("Mas rad roboty? Napis ano, ne, mozna nebo jen ty s cervenymi ocima")
if odpoved_uzivatele == "ano":
    print("Beep boop!")
elif odpoved_uzivatele == "mozna":
    print("Rozhodni se, lidska bytosti!")
elif odpoved_uzivatele == "jen ty s cervenymi ocima":
    print("Zzzap!")
elif odpoved_uzivatele == "ne":
    print("Nuze, roboti te tak jako tak nemaji radi.")
else:
    print("Vaše lidské nesmysly nás urážejí.")


    
